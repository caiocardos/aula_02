public class TiposPrimitivos {
    public static void main(String[] args) {
        byte b = 10; // 1 byte na base 10;
        byte bb = 0b10; // 1 byte na base 2;
        byte bbb = 0x10; // 1 byte na base hex;
        short s = 100; // 2 bytes
        int i = 30; // 4 bytes
        long l = 40; // 8 bytes
        float f = 40.0f; // 4 bytes
        double d = 40.0; // 8 bytes
        char c = 'A'; // 2 bytes
        boolean bool = true; // 1 byte 
    }
}
